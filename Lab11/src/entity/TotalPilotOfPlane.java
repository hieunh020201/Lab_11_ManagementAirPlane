package entity;

public class TotalPilotOfPlane {
    private int id;
    private String type;
    private int totalPilot;

    public TotalPilotOfPlane() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getTotalPilot() {
        return totalPilot;
    }

    public void setTotalPilot(int totalPilot) {
        this.totalPilot = totalPilot;
    }
}
